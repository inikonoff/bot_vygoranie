async def get_ai_response(
    user_text: str, 
    context: str = "", 
    conversation_history: Optional[list] = None
) -> str:
    """
    Получить ответ от AI-психолога с обновленными параметрами.
    
    Args:
        user_text: Текст пользователя
        context: Контекст разговора (устаревший параметр, лучше использовать conversation_history)
        conversation_history: История диалога в формате списка сообщений
        
    Returns:
        Ответ AI
    """
    if not client:
        return "⚠️ Ошибка: ключ API не настроен."

    # Подготовка сообщений
    messages = [{"role": "system", "content": SYSTEM_PROMPT}]
    
    # Добавляем историю диалога, если есть
    if conversation_history:
        for msg in conversation_history[-8:]:  # Берем последние 8 сообщений для контекста
            messages.append(msg)
    elif context:
        # Для обратной совместимости
        messages.append({"role": "system", "content": f"Контекст: {context}"})
    
    # Добавляем текущее сообщение пользователя
    messages.append({"role": "user", "content": user_text})

    try:
        completion = await client.chat.completions.create(
            model=MODEL_NAME,
            messages=messages,
            temperature=0.75,  # Увеличено с 0.7 для лучшей креативности
            max_tokens=800,     # Увеличено с 300 для полных ответов
            top_p=0.9,
            frequency_penalty=0.1,
            presence_penalty=0.1,
        )
        
        response = completion.choices[0].message.content
        
        # Пост-обработка ответа (только очистка префиксов, без укорачивания)
        response = _postprocess_response(response)
        
        return response

    except Exception as e:
        logger.error(f"Groq API Error: {e}", exc_info=True)
        return "Я здесь. Просто побудем в тишине."


def _postprocess_response(response: str) -> str:
    """
    Пост-обработка ответа - только очистка префиксов, без агрессивного укорачивания.
    """
    # Убираем возможные префиксы типа "Ассистент:"
    if response.startswith("Ассистент:") or response.startswith("Assistant:"):
        response = response.split(":", 1)[1].strip()
    
    return response.strip()
