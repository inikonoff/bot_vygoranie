async def get_ai_response(
    user_text: str,
    context: str = "",
    user_context: Optional[dict] = None,
    conversation_history: Optional[list] = None,
    session_mode: str = "",
) -> str:
    """
    Ответ психолога на сообщение пользователя.

    session_mode: 'support' | 'understand' | 'tools' | ''
    """
    client = await get_client()
    if not client:
        logger.warning("Groq client недоступен")
        return "Что-то пошло не так на моей стороне. Попробуй ещё раз."

    # Формируем контекст из user_context
    if user_context and not context:
        u = user_context.get("user", {})
        logs = user_context.get("recent_logs", [])
        parts = []
        if u.get("first_name"):
            parts.append(f"Имя пользователя: {u['first_name']}")
        if u.get("sphere"):
            parts.append(f"Сфера деятельности: {u['sphere']}")
        if u.get("main_request"):
            parts.append(f"С чем пришёл: {u['main_request']}")
        if logs:
            last = logs[0]
            parts.append(
                f"Последняя запись в дневнике — "
                f"энергия {last.get('energy_level')}/10, "
                f"эмоция: {last.get('emotion')}"
            )
        context = ". ".join(parts)

    # Режим сессии
    mode_map = {
        "support": "Режим сессии: ПОДДЕРЖКА — человек хочет выговориться и быть услышанным.",
        "understand": "Режим сессии: РАЗОБРАТЬСЯ — человек хочет понять что происходит. Используй сократовский диалог.",
        "tools": "Режим сессии: ИНСТРУМЕНТЫ — человек хочет конкретные техники и практику.",
    }
    mode_note = mode_map.get(session_mode, "")

    messages = [{"role": "system", "content": SYSTEM_PROMPT}]

    if context or mode_note:
        system_context = "\n".join(filter(None, [context, mode_note]))
        messages.append({"role": "system", "content": system_context})

    if conversation_history:
        for msg in conversation_history[-10:]:
            messages.append(msg)

    messages.append({"role": "user", "content": user_text})

    logger.debug(f"Groq request: {len(messages)} messages, mode={session_mode or 'auto'}")

    try:
        completion = await client.chat.completions.create(
            model=MODEL_NAME,
            messages=messages,
            temperature=0.8,
            max_tokens=800,
            top_p=0.95,
            frequency_penalty=0.15,
            presence_penalty=0.1,
        )
        response = completion.choices[0].message.content
        return _clean_response(response)

    except Exception as e:
        logger.error(f"Groq API Error: {type(e).__name__}: {e}", exc_info=True)
        error_str = str(e).lower()
        if "rate limit" in error_str:
            return "Слишком много запросов подряд — подожди минуту и попробуй снова."
        elif "authentication" in error_str or "invalid api key" in error_str:
            return "Проблема с подключением к AI. Сообщи администратору."
        return "Что-то пошло не так. Попробуй ещё раз."


